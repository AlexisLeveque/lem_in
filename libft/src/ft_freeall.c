#include "../libft.h"

void	ft_freeall(int nbr, char *str1, char *str2)
{
	if (nbr == 1 || nbr == 3)
		ft_memdel((void**)&str1);
	if (nbr == 2 || nbr == 3)
		ft_memdel((void**)&str2);
}
